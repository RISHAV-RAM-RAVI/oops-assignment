#include <iostream>
using namespace std;
int main()
{
    int a = 55;
    cout << "Initial value of a: " << a << endl;

    // Increment operator
    a++;
    cout << "After incrementing, a: " << a << endl;

    // Decrement operator
    a--;
    cout << "After decrementing, a: " << a << endl;

    return 0;
}